#Madlibs game in python
# it is a word game where you create a story template with blanks for words. 
# You then provide the necessary words and the computer fills in the blanks to make a complete story.

print("Welcome to the Madlibs game")

adjective1 = input ("Enter an adjective1: ")
noun1 = input ("Enter a noun: ")
adjective2 = input ("Enter an adjective2: ")
verb1 = input ("Enter a verb ending with 'ing': ")
adjective3 = input ("Enter an adjective3: ")


print (f" Today I went to the {adjective1} zoo")
print (f"and I saw a {noun1}")
print (f"{noun1} was {adjective2} and {verb1}")
print (f"I was {adjective3}")