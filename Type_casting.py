#Type_casting is the process of converting one data type to another data type.
# For this we use str() int() float() bool()
name = "Gech"
age = 28
gpa = 3.8
is_a_student = True
print(type(name))
print(type(age))
print(type(gpa))
print(type(is_a_student))

# Converting the float gpa into int can be 
gpa = int (gpa)
print(gpa)

# Converting the int age into string can be 
age = str (age)
print(age)
print(type(age))
